class Locale{
    constructor(name, texts){
        this.name = name;
        this.texts = texts;
    }
    getText(key){
        return this.texts[key];
    }
}

let Translate = new (class{
    constructor(){
        this.alternativeLanguage = 'English';
        this.locales_names = [];
        this.locales = [];
        this.locales.add = function(name, texts){
            if(this.find(l=>l.name==name)!==undefined) throw new Error(`This language[${name}] has already been created`);
            if(typeof texts.Language != 'string') throw new Error(`There is an error in the 'Language' key of this language[${name}].`);
            if(typeof texts.OtherLanguages != 'object') throw new Error(`There is an error in the 'OtherLanguages' key of this language[${name}].`);
            if(typeof texts.FeaturesName != 'object') throw new Error(`There is an error in the 'FeaturesName' key of this language[${name}].`);
            if(!Array.isArray(texts.Skills) || texts.Skills.length!=14) throw new Error(`There is an error in the 'Skills' key of this language[${name}].`);
            this.push(new Locale(name, texts));
            Translate.locales_names.push(name);
        }
    }

    existLanguage(name){
        return this.locales_names.includes(name);
    }

    getLocale(language){
        return this.locales.find(locale=>locale.name==language);
    }

    start(activeLanguage=null){
        let alternative_locale = this.getLocale(this.alternativeLanguage);
        if(!(alternative_locale instanceof Locale)) return {'status':'error', 'code':1, 'msg': `Alternative language[${this.alternativeLanguage}] not found!`};
        this.alternative_locale=alternative_locale;
        delete this.locales.add;

        if(activeLanguage!=null){
            let locale = this.getLocale(activeLanguage);
            if(!(locale instanceof Locale)) return {'status':'error', 'code':2, 'msg': `Active language[${activeLanguage}] not found!`};
            this.locale = locale;
        }
        else this.locale = this.alternative_locale;
        return {'status':'success'};
    }

    getText(key, opt={tag:1}){
        if(!$.isPlainObject(opt)) opt={tag:1};
        else if(!opt.hasOwnProperty('tag')) opt.tag=1;

        let text = this.locale.getText(key),
            alternativeText=!1;
        if(text===undefined){
            if(this.locale.name!=this.alternativeLanguage){
                text=this.alternative_locale.getText(key);
                alternativeText=!0;
            }
        }

        let type = typeof text;
        if(type == 'undefined'){
            if(opt.tag) return `<font style="background-color:black; color:red; border:1px solid red; font-size:10px;">${key}</font>`;
            else return `Key[${key}] is untranslated`;
        }
        else if(!['string', 'function'].includes(type)) return text;

        if(type == 'function'){
            if(!opt.hasOwnProperty('args')) opt.args=[];
            else if(!Array.isArray(opt.args)) opt.args=[opt.args];
            text=text.apply(null, opt.args);
        }

        if(opt.tag){
            if(alternativeText) text= `<font style="border-bottom:1px dashed; cursor:help; font-style:italic;" title="${this.getText('NotTranslated', {tag:0})}">${text}</font>`;
            if(type=='string') text=`<st k=${key}>${text}</st>`;
            else text=`<ft k=${key} args="${encodeURIComponent(JSON.stringify(opt.args))}">${text}</ft>`;
        }
        return text;
    }

    changeLanguage(language, selectedIndex){
        let locale = this.getLocale(language);
        if(!(locale instanceof Locale)) return {'status':'error', 'msg':`changeLocale: Language[${language}] not found!`};
        this.locale = locale;
        let self = this;

        //Translate: String Text Tags(ST Tags)
        $('st').each(function(){
            let key = $(this).attr('k');
            if(key==undefined) return;
            let st = $(self.getText(key, {tag:1}));
            $(this).html(st.html());
        });

        //Translate: Function Text Tags(FT Tags)
        $('ft').each(function(){
            let key = $(this).attr('k');
            if(key==undefined) return;
            let ft = $(self.getText(key, {args:JSON.parse(decodeURIComponent($(this).attr('args'))) || [], tag:1}));
            $(this).html(ft.html());
        });

        let generateText = r=>{ // Format: key_[args] or key_format or key_[args]_format
            r=r.split('_');
            let key=r[0];
            if(r.length==1) return self.getText(key, {tag:0});
            else if(r.length==2){
                if(r[1].substr(0,3)=="%5B") return self.getText(key, {tag:0, args:JSON.parse(decodeURIComponent(r[1]))});
                else return r[1].replace('{X}', self.getText(key, {tag:0}));
            }
            return r[2].replace('{X}', self.getText(key, {tag:0, args:JSON.parse(decodeURIComponent(r[1]))}));
        }

        //Translate others:
        $('[tool_tt]').each(function(){ //Tool Title Text
            $(this).attr('title', generateText($(this).attr('tool_tt')));
        });
        $('[tool_pt]').each(function(){ //Tool PlaceHolder Text
            $(this).attr('placeholder', generateText($(this).attr('tool_pt')));
        });
        $('[tool_vt]').each(function(){ //Tool Value Text
            $(this).val(generateText($(this).attr('tool_vt')));
        });
        $('[tool_ot]').each(function(){ //Tool Option Text
            $(this).html(generateText($(this).attr('tool_ot')));
        });

        //Translate Trainer and Y.Trainer Options
        $('#AntrenörSeviyesi > option').add($('#GAntrenörSeviyesi > option')).each(function(){
            $(this).text(this.value+' '+self.getText('SortLevel', {tag:0})+(this.textContent.endsWith(' *')?' *':'')); //NOT:Seçili optionların sonunda ' *' bulunur.
        });

        //Translate Features Names in Tool Menu
        $('#featureList > tbody > tr').each(function(){
            var l = $(this).find('label').first();
            l.html(self.locale.texts.FeaturesName[l.attr('k')]);
        });

        //Translate Game Language Select's Options
        $('#gameLanguage > option').each(function(i){
            if(i!=selectedIndex) $(this).text(self.locale.texts.OtherLanguages[this.value]);
            else $(this).text(self.locale.texts.Language+' *'); //Seçili dile ' *' ekleniyor.
        });

        return {'status':'success'};
    }
})();

